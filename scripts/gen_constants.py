def verify_all(groups):
    max_err = 0.0
    n_checked = 0
    for g in groups:
        if g['kind'] == 'scalar':
            err = verify_dd(g['exact'], g['hi'], g['lo'], g['name'])
            max_err = max(max_err, err)
            n_checked += 1
        elif g['kind'] == 'array':
            for i in range(len(g['hi'])):
                err = verify_dd(g['exact'][i], g['hi'][i], g['lo'][i],
                                f"{g['name']}[{i}]")
                max_err = max(max_err, err)
                n_checked += 1
    return max_err, n_checked


